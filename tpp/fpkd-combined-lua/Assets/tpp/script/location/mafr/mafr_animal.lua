function mafr_animal.OnAllocate()
	mvars.loc_locationAnimalSettingTable = mafr_animal
	TppScriptBlock.RegisterCommonBlockPackList( "animal_block", mafr_animal.animalBlockPackList )
end

function mafr_animal.OnMissionCanStart()
	
	TppMotherBaseManagement.RegisterDataBaseAnimalHabitat{
		locationId = TppDefine.LOCATION_ID.MAFR,
		areas = mafr_animal.habitatTable,
		wholeAreaMembers = mafr_animal.captureAnimalList.wholeArea,
	}

	
	local blockId = ScriptBlock.GetScriptBlockId("animal_block")
	if blockId == ScriptBlock.SCRIPT_BLOCK_ID_INVALID then
		return
	end

	Fox.Log("animal block is exist. animal block load message setting activate.")

	mafr_animal.Messages = function()
		return
		Tpp.StrCode32Table{
			Block = {
				{
					msg = "StageBlockCurrentSmallBlockIndexUpdated",
					func = function( x, y )
						TppAnimalBlock.UpdateLoadAnimalBlock( x, y )
					end,
				},
			},
		}
	end

	mafr_animal.messageExecTable = Tpp.MakeMessageExecTable( mafr_animal.Messages() )

	mafr_animal.OnMessage = function(sender, messageId, arg0, arg1, arg2, arg3, strLogText )
		Tpp.DoMessage( mafr_animal.messageExecTable,TppMission.CheckMessageOption, sender, messageId, arg0, arg1, arg2, arg3, strLogText)
	end

	mafr_animal.MAX_AREA_NUM = #mafr_animal.animalAreaSetting

	
	local x, y = Tpp.GetCurrentStageSmallBlockIndex()
	TppAnimalBlock.UpdateLoadAnimalBlock( x, y )

	if DEBUG then
		mvars.debug_currentBlock = {}
	end
end

return mafr_animal

