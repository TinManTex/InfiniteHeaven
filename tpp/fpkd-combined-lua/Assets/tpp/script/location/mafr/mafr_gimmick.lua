mafr_gimmick.Messages = function()
	return
	Tpp.StrCode32Table{
		GameObject = {
			{
				msg		= "BreakGimmick",
				func	= TppGimmick.BreakGimmick,
				option = { isExecMissionPrepare = true },
			},
			{
				msg		= "SwitchGimmick",
				func	= TppGimmick.SwitchGimmick,
				option = { isExecMissionPrepare = true },
			},
			nil
		}
	}
end





function mafr_gimmick.OnInitialize()
	TppGimmick.SetUpIdentifierTable( mafr_gimmick.gimmickIdentifierParamTable )
	TppGimmick.SetUpBreakConnectTable( mafr_gimmick.gimmickBreakConnectTable )
	TppGimmick.SetUpConnectVisibilityTable( mafr_gimmick.gimmickVisibilityiConnectTable )
	TppGimmick.SetUpConnectPowerCutTable( mafr_gimmick.gimmickPowerCutConnectTable )
	mafr_gimmick.messageExecTable = Tpp.MakeMessageExecTable( mafr_gimmick.Messages() )
	Gimmick.RegistGimmickNameIdentifier ( mafr_gimmick.gimmickIdentifierParamTable )
end

function mafr_gimmick.OnMessage(sender, messageId, arg0, arg1, arg2, arg3, strLogText )
	Tpp.DoMessage( mafr_gimmick.messageExecTable,TppMission.CheckMessageOption, sender, messageId, arg0, arg1, arg2, arg3, strLogText)
end

return mafr_gimmick
